<template>
  <div class="row">
    <h2 v-if="isFirst">欢迎来搜索</h2>
    <h2 v-else-if="isLoading">搜索中</h2>
    <h2 v-else-if="errMsg">请求失败 --- {{ errMsg }}</h2>
    <div v-else class="card" v-for="(user, index) in users" :key="user.username">
      <a :href="user.userurl" target="_blank">
        <img :src="user.userimg" style='width:100px' />
      </a>
      <p class="card-text">{{ user.username }}</p>
    </div>
  </div>
</template>

<script>
import axios from 'axios'
export default {
  name: "",
  data() {
    return {
      isFirst: true,
      isLoading: false,
      errMsg: '',
      users: []
    }
  },
  async mounted() {
    this.$bus.$on('searchAjax',await this.searchAjax)
  },
  methods: {
    // async await 是使用同步代码实现异步效果

    // async函数代表这是一个异步函数,async函数返回的是promise

    async searchAjax(q) {
      this.isFirst = false
      this.isLoading = true

      try {
        const response = await axios({
          // url:'https://api.github.com/search/users?q='+keyword
          // url:'https://api.github.com/search/users?q=${keyword}'
          url: 'https://api.github.com/search/users',
          method: 'get',
          params: {
            q
          }
        })
        this.isLoading = false

        // foreach map filter some every reduce 都暗含遍历
        // map
        // 功能：加工数组，根据已有的数组创建新数组，新数组当中每一项和老数组当中的每一项对应有关系
        // 参数：回调函数（item，index，arr）每个数组项都会执行回调函数，返回一个新的项，放在数组中
        // 返回值：把每一项都返回的新项组成的新数组返回

        this.users = response.data.items.map(item => ({
          username: item.login,
          userurl: item.url,
          userimg: item.avatar_url
        }))
        console.log(response.data)
      } catch (error) {
        this.isLoading = false
        this.errMsg = error.message
        console.log(error.message)
      }
    }
  }
}
</script>

